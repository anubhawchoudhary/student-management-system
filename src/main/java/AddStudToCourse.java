    private void jButton_ADDSTUDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ADDSTUDActionPerformed
        
        Student st=new Student();
        String cc=jTextField_coursecode.getText();
        String cn=jTextField_COURSENAME.getText();
        
        int marks=0;
        float atd=100;
        String usn=jTextField_USN.getText();
        if(!usn.equals(this.myusn))
        {
            this.flag=0;
        }
        String yr=jTextField_YEAR.getText();
        if(cc.equals("") || cn.equals("") || usn.equals("") || yr.equals("") || jTextField_CREDITS.getText().equals("") || jTextField_SEM.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "NO FIELD SHOULD BE EMPTY");
        }
        else
        {
            if(flag == 0)
            {
                JOptionPane.showMessageDialog(rootPane, "FIRST CHECK USN BY CLICKING ON DETAIL");
            }
            else
            {
                int credit=Integer.parseInt(jTextField_CREDITS.getText());
                int sem=Integer.parseInt(jTextField_SEM.getText());
                int res=st.AddStudentToCourse(cc, cn, credit, sem, marks, atd, usn, this.tid, yr);
                if(res == -1)
                {
                    JOptionPane.showMessageDialog(rootPane, "DATA ALREADY EXIST");
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "STUDENT ADDED");
                }
                
            }
        }
        
        
    }//GEN-LAST:event_jButton_ADDSTUDActionPerformed

