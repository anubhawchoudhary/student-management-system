    private void jButton_detailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_detailActionPerformed
        String usn=jTextField_USN.getText();
        Connection con=MyConnection.getConnection();
        PreparedStatement ps;
        try {
            ps=con.prepareStatement("SELECT sname, section , department FROM student WHERE usn=?");
            ps.setString(1, usn);
            
            ResultSet rs=ps.executeQuery();
            String sname,dept,sec;
            if(rs.next())
            {
                sname=rs.getString("sname");
                dept=rs.getString("department");
                sec=rs.getString("section");
                jLabel_name1.setText(sname);
                jLabel_dept.setText(dept);
                jLabel_sec.setText(sec);
                this.flag=1;
                this.myusn=usn;
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "USN does not exist");
                jTextField_USN.setText("");
                jLabel_name1.setText("");
                jLabel_dept.setText("");
                jLabel_sec.setText("");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AddStudToCourse.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_detailActionPerformed

